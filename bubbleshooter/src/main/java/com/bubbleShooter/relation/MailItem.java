package com.bubbleShooter.relation;

public class MailItem {
	private int itemId;
	private int itemType;
	private int itemCount;
	private Integer grade = null;
	private Integer level = null;
	private Integer upGrade = null;
	private Integer skill1 = null;
	private Integer skill2 = null;
	private Integer skill3 = null;

	public int getItemId() {
		return itemId;
	}

	public void setItemId(int itemId) {
		this.itemId = itemId;
	}

	public int getItemType() {
		return itemType;
	}

	public void setItemType(int itemType) {
		this.itemType = itemType;
	}

	public int getItemCount() {
		return itemCount;
	}

	public void setItemCount(int itemCount) {
		this.itemCount = itemCount;
	}

	public Integer getGrade() {
		return grade;
	}

	public void setGrade(Integer grade) {
		this.grade = grade;
	}

	public Integer getLevel() {
		return level;
	}

	public void setLevel(Integer level) {
		this.level = level;
	}

	public Integer getUpGrade() {
		return upGrade;
	}

	public void setUpGrade(Integer upGrade) {
		this.upGrade = upGrade;
	}

	public Integer getSkill1() {
		return skill1;
	}

	public void setSkill1(Integer skill1) {
		this.skill1 = skill1;
	}

	public Integer getSkill2() {
		return skill2;
	}

	public void setSkill2(Integer skill2) {
		this.skill2 = skill2;
	}

	public Integer getSkill3() {
		return skill3;
	}

	public void setSkill3(Integer skill3) {
		this.skill3 = skill3;
	}

}
